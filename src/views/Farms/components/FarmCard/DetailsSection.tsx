import React, {useMemo} from 'react'
import useI18n from 'hooks/useI18n'
import styled from 'styled-components'
import { Text, Flex, Link, LinkExternal } from '@pancakeswap-libs/uikit'
import getLiquidityUrlPathParts from 'utils/getLiquidityUrlPathParts'
import { Address } from 'config/constants/types'
import BigNumber from 'bignumber.js'


export interface ExpandableSectionProps {
  isTokenOnly?: boolean
  bscScanAddress?: string
  removed?: boolean
  totalValueFormated?: string
  lpTokenPriceFormated?: string
  lpLabel?: string
  quoteTokenAdresses?: Address
  quoteTokenSymbol?: string
  tokenAddresses: Address
  valueLp: string
  earnedValue : number
  liquidityValue : number
}

const Wrapper = styled.div`
  margin-top: 24px;
`

const StyledLinkExternal = styled(LinkExternal)`
  text-decoration: none;
  font-weight: normal;
  color: ${({ theme }) => theme.colors.text};
  display: flex;
  align-items: center;

  svg {
    padding-left: 4px;
    height: 18px;
    width: auto;
    fill: ${({ theme }) => theme.colors.primary};
  }
`


const DetailsSection: React.FC<ExpandableSectionProps> = ({
  isTokenOnly,
  bscScanAddress,
  removed,
  totalValueFormated,
  lpTokenPriceFormated,
  lpLabel,
  quoteTokenAdresses,
  quoteTokenSymbol,
  tokenAddresses,
  valueLp,
  earnedValue,
  liquidityValue,
}) => {
  const TranslateString = useI18n()
  const liquidityUrlPathParts = getLiquidityUrlPathParts({ quoteTokenAdresses, quoteTokenSymbol, tokenAddresses })

  return (
    <Wrapper>
      <Flex justifyContent="space-between">
        <Text>{TranslateString(316, 'Stake')}:</Text>
        <StyledLinkExternal href={
          isTokenOnly ?
            `https://exchange.pancakeswap.finance//#/swap/${tokenAddresses[process.env.REACT_APP_CHAIN_ID]}`
            :
          `https://exchange.pancakeswap.finance//#/add/${liquidityUrlPathParts}`
        }>
          {lpLabel}
        </StyledLinkExternal>
      </Flex>
      {!removed && (
        <Flex justifyContent="space-between">
          <Text>{TranslateString(23, 'Total Liquidity')}:</Text>
          <Text>{totalValueFormated}</Text>
        </Flex>
      )}
      {!isTokenOnly && (
        <Flex justifyContent="space-between">
          <Text>{TranslateString(999, 'LP price')}:</Text>
          <Text>${lpTokenPriceFormated}</Text>
        </Flex>
      )}
      {!isTokenOnly && (
          <Flex justifyContent="space-between">
            <Text>{TranslateString(999, 'Your liquidity')}:</Text>
            <Text>${liquidityValue.toFixed(2)}</Text>
          </Flex>
      )}
      {isTokenOnly && (
        <Flex justifyContent="space-between">
          <Text>{TranslateString(999, 'Your liquidity')}:</Text>
          <Text>${liquidityValue.toFixed(2)}</Text>
        </Flex>
      )}
        <Flex justifyContent="space-between">
          <Text>{TranslateString(999, 'To Harvest')}:</Text>
          <Text>${earnedValue.toFixed(2)}</Text>
        </Flex>
      <Flex justifyContent="flex-start">
        <Link external href={bscScanAddress} bold={false}>
          {TranslateString(356, 'View on BscScan')}
        </Link>
      </Flex>
    </Wrapper>
  )
}

export default DetailsSection
